# Program do generowania wzoru

# Pierwsza połowa wzoru
for i in range(1, 6):  # Pętla przechodzi przez liczby od 1 do 5
    print("* " * i)  # Drukujemy '*' i razy, oddzielając spacjami

# Druga połowa wzoru
for i in range(4, 0, -1):  # Pętla przechodzi przez liczby od 4 do 1 w odwrotnej kolejności
    print("* " * i)  # Drukujemy '*' i razy, oddzielając spacjami
